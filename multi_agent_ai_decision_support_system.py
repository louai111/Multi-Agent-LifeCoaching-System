# RAG Retrieval

def get_rag_context(vectorstore, query, k=2):
    if not vectorstore:
        return ""
    docs = vectorstore.similarity_search(query, k=k)
    return "\n".join([doc.page_content for doc in docs])

# Single-Agent Decision Function
def single_agent_decision(scenario, vector_stores):
    contexts = []

    # Retrieve from all books
    for book_name, vs in vector_stores.items():
        context = get_rag_context(vs, scenario, k=2)
        if context:
            contexts.append(context)

    combined_context = "\n\n".join(contexts)

    prompt = f"""
Scenario:
{scenario}

Relevant information:
{combined_context}

Instructions:
- Provide a clear, practical, and concise recommendation.
- Use the provided information where relevant.
- If multiple approaches exist, consider them before deciding.
- Focus on actionable steps.
- Keep the answer under 200 words.
"""

    completion = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {
                "role": "system",
                "content": (
                    "You are a helpful, practical assistant that provides clear and grounded advice."
                ),
            },
            {"role": "user", "content": prompt},
        ],
        temperature=0.7,
        max_tokens=400,
    )

    return completion.choices[0].message.content


