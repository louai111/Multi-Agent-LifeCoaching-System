# 3. RAG Setup Function
def setup_rag_for_agent(agent_config):
    file_path = os.path.join(BASE_PATH, agent_config["file_name"])
    if not os.path.exists(file_path):
        print(f"Warning: PDF not found at {file_path}. Agent will run without RAG context.")
        return None

    print(f"Loading and indexing {agent_config['file_name']}...")
    loader = PyPDFLoader(file_path)
    docs = loader.load()

    text_splitter = RecursiveCharacterTextSplitter(chunk_size=1000, chunk_overlap=200)
    splits = text_splitter.split_documents(docs)

    # Using HuggingFace embeddings as they are free and run locally in Colab
    embeddings = HuggingFaceEmbeddings(model_name="all-MiniLM-L6-v2")
    vectorstore = FAISS.from_documents(splits, embeddings)
    return vectorstore

# Build vector stores for all agents
vector_stores = {agent["name"]: setup_rag_for_agent(agent) for agent in agents}

# 4. Core Functions
def get_rag_context(vectorstore, query):
    if not vectorstore:
        return ""
    docs = vectorstore.similarity_search(query, k=3)
    return "\n".join([doc.page_content for doc in docs])

def generate_agent_response(agent, scenario, vectorstore):
    """Generate a single, focused response from an agent based on their perspective and book."""
    # Retrieve relevant book context based on the scenario
    context = get_rag_context(vectorstore, scenario)

    prompt = (
        f"Scenario:\n{scenario}\n\n"
        f"Relevant excerpts from '{agent['book']}':\n{context}\n\n"
        "Provide your analysis and recommendations based on your perspective and the book's principles. "
        "Keep your response concise and practical (under 200 words)."
    )

    try:
        completion = client.chat.completions.create(
            model=agent["model"],
            messages=[
                {"role": "system", "content": agent["system_prompt"]},
                {"role": "user", "content": prompt}
            ],
            temperature=0.7,
            max_tokens=350
        )
        return completion.choices[0].message.content
    except Exception as e:
        return f"[Error generating response: {e}]"

def supervisor_synthesis(scenario, agent_responses):
    """
    Supervisor analyzes all 3 agent responses and provides a final,
    concise, organized, and structured answer.
    """
    responses_text = "\n".join([
        f"\n--- {resp['agent']} ({resp['role']}) ---\n{resp['content']}"
        for resp in agent_responses
    ])

    prompt = (
        f"Scenario:\n{scenario}\n\n"
        f"Analysis from Three Perspectives:\n{responses_text}\n\n"
        "You are the Supervisor. Synthesize these three perspectives into a final, "
        "concise, and well-structured action plan. The output should be:\n"
        # "1. **Key Insights** - Main points across all perspectives (2-3 bullet points)\n" # remove
        "**Recommended Action Plan** - Clear, actionable steps (3-5 steps)\n"
        # "3. **Success Factors** - What to focus on for best results (2-3 points)\n\n" # remove
        "Be concise and practical. Aim for less than 200 words total." # 300-400 words total." # 200
    )

    completion = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {"role": "system", "content":
                "You are a supervisor who synthesizes diverse expert perspectives into clear, "
                "actionable guidance. You are organized, decisive, and strategic."},
            {"role": "user", "content": prompt}
        ],
        temperature=0.7,
        max_tokens=600
    )

    return completion.choices[0].message.content

